			GLOBAL	LAB10
			IMPORT LEDSETUP
			IMPORT LEDOFF
			IMPORT LEDON
			IMPORT delay
			IMPORT DELAYBX
rDELAY 			RN 12
;PINS
PINSEL0			EQU	0XE002C000
IO0BASE			EQU	0XE0028000
IO0PIN			EQU	0X0
IO0SET			EQU 0x4
IO0DIR			EQU	0x8
IO0CLR			EQU 0xC

;LED
LEDTOGGLE		EQU	0X0000FF00
LED0PATTERN		EQU	0X00003300
LED1PATTERN		EQU	0XFFFFFF7F

;IO_DIR
OUTPUT			EQU 0X0000FF00
			AREA lab10, CODE, READONLY
LAB10	
	
			SWI	#1
			;DELAY
			LDR rDELAY, =400000
			BL DELAYBX
			
			SWI #2
			LDR rDELAY, =400000
			BL DELAYBX
			
			SWI #3
			;DELAY
			LDR rDELAY, =400000
			BL DELAYBX
			
			;TOGGLE LED OFF
			BL LEDOFF
			
			B	LAB10
			END